CREATE TABLE pedido (
    id NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario_id NUMBER(19),
    produto_id NUMBER(19),
    quantidade NUMBER(10),
    CONSTRAINT fk_id_usuario FOREIGN KEY (usuario_id) REFERENCES usuario_db.usuario(id),
    CONSTRAINT fk_id_produto FOREIGN KEY (produto_id) REFERENCES produto_db.produto(id)
);